def display_rainbow(colors)
  puts "R: #{colors[1]}, O: #{colors[0]}, Y: #{colors[2]}, G: #{colors[5]}, B: #{colors[4]}, I: #{colors[6]}, V: #{colors[3]}"
end

colors = ['orange', 'red', 'yellow', 'violet', 'blue', 'green', 'indigo']
display_rainbow(colors)
